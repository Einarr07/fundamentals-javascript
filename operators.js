// Comparison operators

const a = 10;
const b = 20;
const c = "10";

console.log("Double equals (==): " + (a == b));
console.log("Strict equals (===): " + (a === c));
console.log("Not equal (!=): " + (a != b));
console.log("Strict not equal (!==): " + (a !== c));
console.log("Greater than (>): " + (a > b));
console.log("Less or equal (<=): " + (a <= b));
